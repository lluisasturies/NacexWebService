package com.nacex.dto;

public class EstadoExpedicion {
	private String codExp;
	private String fecha;
	private String hora;
	private String obs;
	private String estado;
	private String codEstado;
	private String delOrigen;
	private String albaran;
	
	public String getCodExp() {
		return codExp;
	}
	public void setCodExp(String codExp) {
		this.codExp = codExp;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	public String getObs() {
		return obs;
	}
	public void setObs(String obs) {
		this.obs = obs;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getCodEstado() {
		return codEstado;
	}
	public void setCodEstado(String codEstado) {
		this.codEstado = codEstado;
	}
	public String getDelOrigen() {
		return delOrigen;
	}
	public void setDelOrigen(String delOrigen) {
		this.delOrigen = delOrigen;
	}
	public String getAlbaran() {
		return albaran;
	}
	public void setAlbaran(String albaran) {
		this.albaran = albaran;
	}
}
